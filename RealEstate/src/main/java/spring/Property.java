package spring;

public class Property {
	private String property_types;
	private String location;
	private String address;
	private String trading_types;
	private String size;
	private String price;
	private String photo;
	private String message;
	private String host_id;
	
	public String getProperty_types() {
		return property_types;
	}
	public void setProperty_types(String property_types) {
		this.property_types = property_types;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getTrading_types() {
		return trading_types;
	}
	public void setTrading_types(String trading_types) {
		this.trading_types = trading_types;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getHost_id() {
		return host_id;
	}
	public void setHost_id(String host_id) {
		this.host_id = host_id;
	}
	
	
}
